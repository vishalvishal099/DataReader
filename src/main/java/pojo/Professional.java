package pojo;

public class Professional {
     int id;
     int numberOfMemberInFamily;
     String fatherName;
     String mothername;
     String wifeName;

     public int getId() {
          return id;
     }

     public void setId(int id) {
          this.id = id;
     }

     public int getNumberOfMemberInFamily() {
          return numberOfMemberInFamily;
     }

     public void setNumberOfMemberInFamily(int numberOfMemberInFamily) {
          this.numberOfMemberInFamily = numberOfMemberInFamily;
     }

     public String getFatherName() {
          return fatherName;
     }

     public void setFatherName(String fatherName) {
          this.fatherName = fatherName;
     }

     public String getMothername() {
          return mothername;
     }

     public void setMothername(String mothername) {
          this.mothername = mothername;
     }

     public String getWifeName() {
          return wifeName;
     }

     public void setWifeName(String wifeName) {
          this.wifeName = wifeName;
     }


}
